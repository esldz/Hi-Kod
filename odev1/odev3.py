sayi1=float(input("Birinci sayıyı giriniz: "))
sayi2=float(input("İkinci sayıyı giriniz: "))

toplam=sayi1+sayi2
cikarma=sayi1-sayi2
carpma=sayi1*sayi2
bolme=sayi1/sayi2

print(f"{sayi1} + {sayi2} = {toplam}")
print(f"{sayi1} - {sayi2} = {cikarma}")
print(f"{sayi1} * {sayi2} = {carpma}")
print(f"{sayi1} / {sayi2} = {bolme}")
